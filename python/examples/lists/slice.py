letters = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j']

print(letters[0::1])    # ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j']

print(letters[0::2])    # ['a', 'c', 'e', 'g', 'i']

print(letters[1::2])    # ['b', 'd', 'f', 'h', 'j']

print(letters[1:8:2])    # ['b', 'd', 'f', 'h']

print(letters[1:20:3])    # ['b', 'e', 'h']

